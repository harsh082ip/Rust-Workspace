fn main() {
    // Signed Integer
    let x: i32 = 12;
    println!("{}", x);

    // Unsigned integer
    let num: u16 = 123;
    println!("{}", num);

    // Boolean
    let ismale = true;

    // if-else
    if ismale {
        println!("You r a legal male")
    } else {
        println!("Female")
    }
}
